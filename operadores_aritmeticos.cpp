//OPERADORES ARITMÉTICOS:
#include <iostream>
using namespace std;
int main(){
    int a = 10;
    int b = 3;

    cout << "a + b = " << a + b << endl; //adição
    cout << "a - b = " << a - b << endl; //subtração
    cout << "a * b = " << a * b << endl; //multiplicação
    cout << "a / b = " << a / b << endl; //divisão
    cout << "a % b = " << a % b << endl; //resto da divisão
    
    return 0;
}