//OPERADORES RELACIONAIS:
#include <iostream>
using namespace std;

int main(){
    int x = 5;
    int y = 10;

    cout << (x == y) << endl; //igual a
    cout << (x != y) << endl; //diferente de
    cout << (x > y) << endl; //maior que
    cout << (x < y) << endl; //menor que
    cout << (x >= y) << endl; //maior ou igual a 
    cout << (x <= y) << endl; //menor ou igual a

    return 0;
}